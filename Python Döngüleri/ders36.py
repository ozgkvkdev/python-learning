# PYTHON RANGE FONKSİYONU


print("1'den 10'a kadar sayılar:")

for sayi in range(1, 11):
    print(sayi)



# ÇİFT SAYILARI LİSTELEME


print("\n1 ile 20 arasındaki çift sayılar:")

for sayi in range(2, 21, 2):
    print(sayi)



# KÜÇÜK UYGULAMA


print("\n1 ile 5 arasındaki sayıların kareleri:")

for sayi in range(1, 6):
    print(f"{sayi}² = {sayi ** 2}")